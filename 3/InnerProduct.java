import java.io.*;

public class InnerProduct
{
     public static void main(String[] args)
    {
	double[] vec1 ={1.0 , 1.5 , 2.0 , 2.5};
	double[] vec2 ={3.1 , 2.1 , 1.1 , 0.1};
	double dot=0;
	for (int i = 0; i < vec1.length; i++) {
	    dot  += vec1[i]*vec2[i];
	}
	System.out.println("内積は"+dot+"です。");
    }                                                                                                                    
}   

